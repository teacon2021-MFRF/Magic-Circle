package mfrf.magic_circle.magicutil;

public class RGBA {
    public int r;
    public int g;
    public int b;
    public int a;

    public RGBA(double r, double g, double b, double alpha) {
        this.r = (int) r;
        this.g = (int) g;
        this.b = (int) b;
        this.a = (int) alpha;
    }
}
